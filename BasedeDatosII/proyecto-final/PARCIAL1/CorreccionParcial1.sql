=== SUGERENCIA 3 — Usar RAISE_APPLICATION_ERROR en lugar de códigos mágicos ===

Retornar -1 y -10 como códigos de error funciona, pero es frágil:
quien llama a la función debe saber de memoria qué significa cada número.
Una alternativa más robusta y descriptiva:

    RAISE_APPLICATION_ERROR(-20001, 'Paciente no encontrado');
    RAISE_APPLICATION_ERROR(-20002, 'Paciente duplicado - apellido y DNI ambiguos');

Los códigos -20000 a -20999 están reservados para errores de usuario en Oracle.
El error se propaga con un mensaje claro en lugar de un número arbitrario.


=== SUGERENCIA 4 — Manejar TOO_MANY_ROWS explícitamente ===

En el Ejercicio 1, el enfoque con COUNT(*) es correcto y evita TOO_MANY_ROWS.
Pero conviene conocer la excepción para defenderla:

    Si hubieras usado SELECT ... INTO directamente sin COUNT,
    Oracle lanzaría TOO_MANY_ROWS cuando dos pacientes coincidan.
    Tu elección de usar COUNT primero es la más segura y correcta.

Ejemplo de cómo se vería con excepción explícita (alternativa menos recomendada):
