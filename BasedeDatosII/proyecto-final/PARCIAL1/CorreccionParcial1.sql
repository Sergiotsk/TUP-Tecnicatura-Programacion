-- ============================================================
-- EJERCICIO 2
-- Tabla de auditoría + Trigger: trg_logs_consultas
-- Auditoría de cambios sobre la tabla PACIENTES.
-- Se dispara DESPUÉS de INSERT, UPDATE o DELETE.
-- Registra en LOGS_PACIENTES: usuario DB, fecha y motivo.
-- ============================================================

-- Tabla de auditoría donde se registran todos los cambios.
-- GENERATED BY DEFAULT AS IDENTITY: autoincremento automático para el ID (Oracle 12c+).
CREATE TABLE logs_pacientes(
    id      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    usuario VARCHAR2(100),  -- Usuario de base de datos que ejecutó la acción (función USER)
    fecha   DATE,           -- Fecha y hora del evento (función SYSDATE)
    motivo  VARCHAR2(255)   -- Descripción del evento: inserción, actualización o eliminación
);

-- AFTER: el trigger se ejecuta DESPUÉS de que la operación DML impacta la tabla.
-- FOR EACH ROW: granularidad por fila (se ejecuta una vez por cada fila afectada).
-- :NEW → valores nuevos disponibles en INSERT y UPDATE.
-- :OLD → valores anteriores disponibles en UPDATE y DELETE.
CREATE OR REPLACE TRIGGER trg_logs_consultas
    AFTER INSERT OR UPDATE OR DELETE ON pacientes  -- Se activa tras cualquier cambio DML
    FOR EACH ROW                                   -- Una ejecución por fila modificada
DECLARE
    v_motivo VARCHAR2(255);  -- Variable local para construir el mensaje del log
BEGIN
    -- INSERTING, UPDATING, DELETING: predicados booleanos exclusivos de triggers.
    -- Retornan TRUE según el tipo de operación DML que disparó el trigger.
    IF INSERTING THEN
        -- :NEW.id_paciente: ID de la fila recién insertada
        v_motivo := 'Insercion de Paciente ID: ' || :NEW.id_paciente;
    ELSIF UPDATING THEN
        -- :OLD.id_paciente: ID de la fila antes de ser modificada
        v_motivo := 'Actualizacion de Paciente ID: ' || :OLD.id_paciente;
    ELSIF DELETING THEN
        -- :OLD.id_paciente: ID de la fila que está siendo eliminada
        v_motivo := 'Eliminacion de Paciente ID: ' || :OLD.id_paciente;
    END IF;

    -- USER    → nombre del usuario conectado a la sesión de base de datos
    -- SYSDATE → fecha y hora actual del servidor Oracle
    INSERT INTO logs_pacientes(usuario, fecha, motivo)
    VALUES (USER, SYSDATE, v_motivo);
END;
/

-- ---- Bloque de prueba ----
-- SAVEPOINT para poder deshacer los cambios de prueba y dejar la tabla limpia
BEGIN
    SAVEPOINT antes_prueba;

    -- INSERT: dispara la rama INSERTING del trigger
    INSERT INTO pacientes (nombre, apellido, dni, fecha_nacimiento, direccion, telefono, email, id_obra_social)
    VALUES ('sergio', 'tschernitschek', '32384844', TO_DATE('1986-07-01','YYYY-MM-DD'), 'rivadavia', '113', 'sergiots1786@gmail.com', 1);

    -- UPDATE: dispara la rama UPDATING del trigger
    UPDATE pacientes SET telefono = '1161930819' WHERE dni = '32384844';

    -- DELETE: dispara la rama DELETING del trigger
    DELETE FROM pacientes WHERE dni = '32384844';

    -- Cursor implícito en FOR-LOOP: itera los 3 registros generados en LOGS_PACIENTES
    FOR logs IN (SELECT * FROM LOGS_PACIENTES) LOOP
        DBMS_OUTPUT.PUT_LINE('Usuario: ' || logs.USUARIO || ' | LOG ID: ' || logs.ID || ' | Motivo: ' || logs.MOTIVO);
    END LOOP;

    -- Deshacemos los cambios de prueba para no contaminar la tabla
    ROLLBACK TO antes_prueba;
END;
/


-- ============================================================
-- EJERCICIO 3
-- Función: dias_internacion
-- Retorna los días que lleva internado un paciente activo.
-- Retorna -1  si el paciente ya fue dado de alta (tiene fecha_egreso).
-- Retorna -10 si el paciente nunca fue internado.
-- ============================================================

CREATE OR REPLACE FUNCTION dias_internacion(
    p_id_paciente IN internaciones.id_paciente%TYPE  -- ID del paciente a consultar
) RETURN NUMBER
IS
    -- %TYPE ancla el tipo de la variable al tipo real de la columna en la tabla.
    -- Ventaja: si la columna cambia de tipo, la función se adapta automáticamente.
    v_fecha_ingreso internaciones.fecha_ingreso%TYPE;
    v_fecha_egreso  internaciones.fecha_egreso%TYPE;
BEGIN
    -- Buscamos una internación ACTIVA: fecha_egreso IS NULL significa sin alta médica.
    -- Si no hay fila que cumpla → Oracle lanza NO_DATA_FOUND automáticamente.
    SELECT fecha_ingreso, fecha_egreso
      INTO v_fecha_ingreso, v_fecha_egreso
      FROM internaciones
     WHERE id_paciente = p_id_paciente
       AND fecha_egreso IS NULL;  -- Condición clave: solo internaciones activas

    -- TRUNC() elimina decimales del resultado de la resta de fechas,
    -- devolviendo días enteros sin fracción horaria.
    -- CURRENT_DATE: fecha actual según la zona horaria de la sesión.
    RETURN TRUNC(CURRENT_DATE - v_fecha_ingreso);

EXCEPTION
    -- NO_DATA_FOUND: el SELECT INTO no encontró fila con fecha_egreso IS NULL.
    -- Puede ser: dado de alta (tiene fecha_egreso) o nunca internado (sin registros).
    WHEN NO_DATA_FOUND THEN
        DECLARE
            v_cont NUMBER;  -- Contador para distinguir los dos casos posibles
        BEGIN
            -- Contamos TODAS las internaciones del paciente (con o sin fecha_egreso)
            SELECT COUNT(*)
              INTO v_cont
              FROM internaciones
             WHERE id_paciente = p_id_paciente;

            -- Sin registros en absoluto: el paciente nunca fue internado
            IF v_cont = 0 THEN
                RETURN -10;
            ELSE
                -- Hay registros pero todos tienen fecha_egreso → fue dado de alta
                RETURN -1;
            END IF;
        END;
END;
/

-- ---- Bloque de prueba (CORREGIDO) ----
-- BUG ORIGINAL: el IF evaluaba v_res ANTES de llamar a la función (v_res era NULL).
-- NULL > 0 siempre es FALSE en Oracle → la función nunca se ejecutaba.
-- CORRECCIÓN: primero ejecutar la función, después evaluar el resultado.
DECLARE
    v_id_paciente internaciones.id_paciente%TYPE := &Ingrese_ID_de_paciente;
    v_res         NUMBER;
BEGIN
    -- PASO 1: ejecutar la función y guardar el resultado
    v_res := dias_internacion(v_id_paciente);

    -- PASO 2: evaluar el resultado (ahora v_res tiene un valor, no es NULL)
    IF    v_res = -10 THEN DBMS_OUTPUT.PUT_LINE('Paciente nunca ingresado a internacion');
    ELSIF v_res = -1  THEN DBMS_OUTPUT.PUT_LINE('Paciente dado de alta');
    ELSE                   DBMS_OUTPUT.PUT_LINE('Dias de internacion activa: ' || v_res);
    END IF;
END;
/


-- ============================================================
-- EJERCICIO 4
-- Procedimiento: listado_por_obra_social
-- Lista todos los pacientes agrupados por obra social.
-- Encabezado por obra social: nombre y dirección.
-- Detalle por paciente: apellido, nombre y email.
-- ============================================================

CREATE OR REPLACE PROCEDURE listado_por_obra_social AS

    -- Cursor explícito c_obras: recorre todas las obras sociales.
    -- Un cursor explícito da control manual sobre la consulta.
    CURSOR c_obras IS
        SELECT id_obra_social, nombre, direccion
        FROM obras_sociales;

    -- Cursor parametrizado c_pacientes: recibe el ID de obra social como parámetro.
    -- Permite reutilizar el mismo cursor con distintos valores en cada iteración.
    CURSOR c_pacientes(p_id obras_sociales.id_obra_social%TYPE) IS
        SELECT apellido, nombre, email
        FROM pacientes
        WHERE id_obra_social = p_id;  -- Filtra pacientes de la obra social actual

BEGIN
    -- FOR...LOOP sobre cursor: Oracle abre, avanza y cierra el cursor automáticamente.
    -- La variable "os" es un registro implícito con los campos de la fila actual.
    FOR os IN c_obras LOOP

        -- Imprimimos encabezado por cada obra social
        DBMS_OUTPUT.PUT_LINE('=== OBRA SOCIAL: ' || os.nombre || ' | Dirección: ' || os.direccion || ' ===');

        -- Loop anidado: pasamos el ID de la obra social actual al cursor parametrizado.
        -- Si la obra social no tiene pacientes, este loop simplemente no ejecuta.
        FOR p IN c_pacientes(os.id_obra_social) LOOP
            DBMS_OUTPUT.PUT_LINE('  ' || p.apellido || ', ' || p.nombre || ' - ' || p.email);
        END LOOP;

    END LOOP;
END listado_por_obra_social;
/

-- ---- Bloque de prueba ----
BEGIN
    listado_por_obra_social;  -- Invocamos el procedimiento sin parámetros
END;
/


