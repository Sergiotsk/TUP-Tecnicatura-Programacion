        WHEN TOO_MANY_ROWS THEN RETURN -10;
        WHEN NO_DATA_FOUND THEN RETURN 0;


=== SUGERENCIA 5 — Consistencia entre CURRENT_DATE y SYSDATE ===

En el Ejercicio 2 usás SYSDATE y en el Ejercicio 3 usás CURRENT_DATE.
Ambas retornan la fecha actual, pero:
  - SYSDATE    → zona horaria del SERVIDOR (más común en sistemas Oracle)
  - CURRENT_DATE → zona horaria de la SESIÓN del usuario

En un sistema donde servidor y clientes están en distintas zonas horarias,
mezclar ambas puede generar diferencias horarias difíciles de depurar.
Recomendación: elegí una sola y usala en todo el proyecto.
Estándar histórico de Oracle: SYSDATE.


