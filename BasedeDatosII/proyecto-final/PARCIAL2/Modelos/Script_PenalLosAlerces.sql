CREATE TABLE CRIMINALES(
    ID NUMBER(6) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, 
    NOMBRE VARCHAR2(30 BYTE) NOT NULL, 
    SEXO VARCHAR2(12 BYTE) NOT NULL, 
    EDAD NUMBER(3,0), 
    ALTURA VARCHAR2(10 BYTE), 
    COLOR_PELO VARCHAR2(10 BYTE), 
    COLOR_OJOS VARCHAR2(10 BYTE), 
    TATUAJES VARCHAR2(3 BYTE), 
    ANTEOJOS VARCHAR2(3 BYTE), 
    CICATRICES VARCHAR2(3 BYTE), 
    BARBA VARCHAR2(3 BYTE), 
    CALZADO VARCHAR2(10 BYTE)
);


CREATE TABLE CATEGORIAS_PENITENCIARIOS (
    ID NUMBER(6) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, 
    DESCRIPCION VARCHAR2(60 BYTE), 
    SUELDO NUMBER(10,2)
);


CREATE TABLE DETECTIVES (
   	ID NUMBER(6) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, 
	NOMBRE VARCHAR2(30 BYTE), 
	APELLIDO VARCHAR2(30 BYTE), 
	SEXO VARCHAR2(12 BYTE), 
	FECHA_ALTA DATE
);


CREATE TABLE crimenes (
   	ID NUMBER(6) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, 
    tipo_crimen      VARCHAR2(30 BYTE) NOT NULL,
    fecha_crimen     DATE,
    lugar_crimen     VARCHAR2(100 BYTE) NOT NULL,
    damnificado_cuil VARCHAR2(11 BYTE) NOT NULL,
    d_apellido       VARCHAR2(50 BYTE) NOT NULL,
    d_nombre         VARCHAR2(50 BYTE) NOT NULL,
    detective        NUMBER(6) NOT NULL
);

CREATE TABLE prisiones (
   	ID NUMBER(6) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, 
    nombre           VARCHAR2(60 BYTE) NOT NULL,
    direccion        VARCHAR2(100 BYTE) NOT NULL,
    director_prision NUMBER(6) NOT NULL
);


CREATE TABLE servicio_penitenciario (
   	ID NUMBER(6) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, 
    nombre    VARCHAR2(60 BYTE) NOT NULL,
    direccion VARCHAR2(100 BYTE) NOT NULL,
    categoria NUMBER(6) NOT NULL
);

CREATE TABLE  CRIMINALES_PRISION (
   	CRIMINAL NUMBER(6,0), 
	PRISION NUMBER(6,0), 
	CRIMEN NUMBER(6,0), 
	FECHA_INGRESO DATE, 
	FECHA_EGRESO DATE
);


CREATE TABLE sospechosos_crimen (
    crimen     NUMBER(6),
    sospechoso NUMBER(6)
);

ALTER TABLE PRISIONES
